


public class Voter {
	
	private String uname,emailid,phone,pass;
	
	public Voter() {
		super();
	}

	public Voter(String uname, String emailid, String phone, String pass) {
		super();
		this.uname = uname;
		this.emailid = emailid;
		this.phone = phone;
		this.pass = pass;
	}

	public String getUname() {
		return uname;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public String getEmailid() {
		return emailid;
	}

	public void setEmailid(String emailid) {
		this.emailid = emailid;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}

	
	

}
